package uk.ac.babraham.redotable.utilities.fileFilters;

import java.io.File;

import javax.swing.filechooser.FileFilter;

/**
 * A File filter representing SVG Files
 */
public class FastAFileFilter extends FileFilter {

	/* (non-Javadoc)
	 * @see javax.swing.filechooser.FileFilter#accept(java.io.File)
	 */
	@Override
	public boolean accept(File f) {
		if (f.isDirectory() || f.getName().toLowerCase().endsWith(".fasta") || f.getName().toLowerCase().endsWith(".fa")) {
			return true;
		}
		else {
			return false;
		}
	}

	/* (non-Javadoc)
	 * @see javax.swing.filechooser.FileFilter#getDescription()
	 */
	@Override
	public String getDescription() {
		return "FastA Files";
	}


}
